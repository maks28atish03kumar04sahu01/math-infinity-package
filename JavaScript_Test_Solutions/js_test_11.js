// DIVISION OPERATIONS WITH GIVEN INPUTS AND CHOICE

import {divisionOperation} from "mathinfinity";

console.log(`Quotient: ${divisionOperation(14, '8', 3, 'QUOTIENT')}`); // Quotient: 2
console.log(`Remainder: ${divisionOperation(140, 8, "3", "Remainder")}`); // Remainder: 116
console.log(`Dividend: ${divisionOperation(`250`, 28, "17", "DiViDeNd")}`); // Dividend: 7017
console.log(`Divisior: ${divisionOperation(360, 17, "20", "dIvIsOr")}`); // Divisior: 20

